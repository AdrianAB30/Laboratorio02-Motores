using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Imput_Controller : MonoBehaviour
{
    public static event Action<Vector2> Movement2D;
    public static event Action<float> AxisMovement;
    public static event Action OnJump;

    private void Update()
    {
        DirectionAxis();
        Direction();
        ReadJump();
    }

    protected void DirectionAxis()
    {
        Vector2 movement = new Vector2(Input.GetAxisRaw("Horizontal"),0);
        Movement2D?.Invoke(movement);
    }
    protected void Direction()
    {
        AxisMovement?.Invoke(Input.GetAxisRaw("Horizontal"));
    }
    protected void ReadJump()
    {
        if (Input.GetKeyDown(KeyCode.Space))
        {
            OnJump?.Invoke();
        }
    }
}
